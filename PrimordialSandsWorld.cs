        public bool PlaceVillage(int i, int j)
        {
            if (!WorldGen.SolidTile(i, j + 1))
            {
                return false;
            }
            if (Main.tile[i, j].active())
            {
                return false;
            }
            for (int y = 0; y < VillageShape.GetLength(0); y++)
            {
                for (int x = 0; x < VillageShape.GetLength(1); x++)
                {
                    int k = i - 3 + x;
                    int l = j - 6 + y;
                    if (WorldGen.InWorld(k, l, 30))
                    {
                        Tile tile = Framing.GetTileSafely(k, l);
                        switch (VillageShape[y, x])
                        {
                            case 0:
                                tile.active(false); //Air
                                break;
                            case 1:
                                tile.type = TileID.GrayBrick; //Brick
                                tile.active(true);
                                break;
                            case 2:
                                tile.type = TileID.WoodBlock; //Wood
                                tile.active(true);
                                break;
                            case 3:
                                tile.type = TileID.WoodBlock; //Slope
                                tile.active(true);
                                tile.slope(1);
                                break;
                            case 4:
                                tile.type = TileID.WoodBlock; //Slope
                                tile.active(true);
                                tile.slope(2);
                                break;
                            case 7:
                                tile.liquid = 255; //Water
                                break;
                            case 8:
                                tile.type = TileID.Platforms; //Platform
                                tile.active(true);
                                break;
                            case 9:
                                tile.type = TileID.ClosedDoor; //Platform
                                tile.active(true);
                                break;
                        }
                        switch (VillageWall[y, x])
                        {
                            case 1:
                                tile.wall = WallID.Wood; //Brick
                                break;
                            case 2:
                                tile.wall = WallID.GrayBrick; //Decor
                                break;
                            case 3:
                                tile.wall = WallID.Planked; //Decor
                                break;
                            case 4:
                                tile.wall = WallID.StoneSlab; //Decor
                                break;
                            case 5:
                                tile.wall = WallID.Glass; //Decor
                                break;
                            case 6:
                                tile.wall = WallID.WoodenFence; //Decor
                                break;
                        }
                        switch (VillageFlooring[y, x])
                        {
                            case 1:
                                tile.type = TileID.GrayBrick; //Brick
                                tile.active(true);
                                break;
                            case 2:
                                tile.type = TileID.GrayBrick; //Half Block
                                tile.active(true);
                                tile.halfBrick(true);
                                break;
                        }
                    }
                }
            }
            return true;
        }
