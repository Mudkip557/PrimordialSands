        public static bool OrcsAcquisitionUp = false;
        public static bool downedOrcsAcquisition = false;

        public override void Initialize()
        {
            Main.invasionSize = 0;
            OrcsAcquisitionUp = false;
            downedOrcsAcquisition = false;
        }
        public override TagCompound Save()
        {
            var downed = new List<string>();
            if (downedOrcsAcquisition) downed.Add("orcsAcquisition");

            return new TagCompound {
                {"downed", downed}
            };
        }

        public override void Load(TagCompound tag)
        {
            var downed = tag.GetList<string>("downed");
            downedOrcsAcquisition = downed.Contains("orcsAcquisition");
        }

        public override void NetSend(BinaryWriter writer)
        {
            BitsByte flags = new BitsByte();
            flags[0] = downedOrcsAcquisition;
            writer.Write(flags);
        }

        public override void NetReceive(BinaryReader reader)
        {
            BitsByte flags = reader.ReadByte();
            downedOrcsAcquisition = flags[0];
        }

        public override void PostUpdate()
        {
            if (OrcsAcquisitionUp)
            {
                if (Main.invasionX == (double)Main.spawnTileX)
                {
                    OrcsAcquisition.CheckOrcsAcquisitionProgress();
                }
                OrcsAcquisition.UpdateOrcsAcquisition();
            }
        }

        public override void ModifyWorldGenTasks(List<GenPass> tasks, ref float totalWeight)
        {
            int ShiniesIndex = tasks.FindIndex(genpass => genpass.Name.Equals("Shinies"));
            if (ShiniesIndex != -1)
            {
                tasks.Insert(ShiniesIndex + 1, new PassLegacy("Primordial Sands Ores", PrimordialSandsOres));
            }

            int LivingTreesIndex = tasks.FindIndex(genpass => genpass.Name.Equals("Living Trees"));
            if (LivingTreesIndex != -1)
            {
                tasks.Insert(LivingTreesIndex + 1, new PassLegacy("Post Terrain", delegate (GenerationProgress progress)
                {
                    progress.Message = "Building Settlements...";
                    MakeIgloo();
                }));
            }

        }

        private void PrimordialSandsOres(GenerationProgress progress)
        {
            progress.Message = "Generating Minerals...";
            for (int k = 0; k < (int)((double)(Main.maxTilesX * Main.maxTilesY) * 6E-05); k++)
            {
                int x = WorldGen.genRand.Next(0, Main.maxTilesX);
                int y = WorldGen.genRand.Next((int)WorldGen.rockLayer, Main.maxTilesY);
                WorldGen.TileRunner(x, y, (double)WorldGen.genRand.Next(3, 6), WorldGen.genRand.Next(2, 6), mod.TileType("CarbonTile"), false, 0f, 0f, false, true);
            }
        }

