        public bool PlaceIgloo(int i, int j)
        {
            if (!WorldGen.SolidTile(i, j + 1))
            {
                return false;
            }
            if (Main.tile[i, j].active())
            {
                return false;
            }
            for (int y = 0; y < IglooShape.GetLength(0); y++)
            {
                for (int x = 0; x < IglooShape.GetLength(1); x++)
                {
                    int k = i - 3 + x;
                    int l = j - 6 + y;
                    if (WorldGen.InWorld(k, l, 30))
                    {
                        Tile tile = Framing.GetTileSafely(k, l);
                        switch (IglooShape[y, x])
                        {
                            case 0:
                                tile.active(false); //Air
                                break;
                            case 1:
                                tile.type = TileID.IceBrick; //Brick
                                tile.active(true);
                                break;

                            case 2:
                                tile.type = TileID.IceBrick; //Brick, Sloped
                                tile.active(true);
                                tile.halfBrick(true);
                                break;

                            case 3:
                                tile.type = TileID.SnowBlock; //Terrain
                                tile.active(true);
                                break;

                            case 4:
                                tile.type = TileID.IceBlock; //Terrain
                                tile.active(true);
                                break;
                            case 5:
                                tile.type = TileID.Containers; //Ice Chest
                                tile.active(true);
                                break;
                            case 6:
                                tile.type = TileID.IceBrick; //Slope
                                tile.active(true);
                                tile.slope(2);
                                break;
                            case 7:
                                tile.type = TileID.IceBrick; //Slope
                                tile.active(true);
                                tile.slope(1);
                                break;
                            case 8:
                                tile.type = TileID.IceBrick; //Slope
                                tile.active(true);
                                tile.slope(3);
                                break;
                            case 9:
                                tile.type = TileID.IceBrick; //Slope
                                tile.active(true);
                                tile.slope(4);
                                break;
                        }
                        switch (IglooWall[y, x])
                        {
                            case 1:
                                tile.wall = WallID.IceBrick; //Brick
                                break;
                            case 2:
                                tile.wall = WallID.BorealWoodFence; //Terrain
                                break;
                            case 3:
                                tile.wall = WallID.SnowWallUnsafe; //Terrain
                                break;
                        }
                        switch (IglooDecor[y, x])
                        {
                            case 1:
                                tile.type = TileID.Chairs; //Sitboye
                                break;
                            case 2:
                                tile.type = TileID.Tables; //FoodHolderBoye
                                break;
                            case 3:
                                tile.type = TileID.ClosedDoor; //DontLetItHitYouOnTheWayOutBoye
                                break;
                        }
                    }
                }
            }
            return true;
        }
